package kr.or.bit;
/*
 * 클래스는 설계도이다, 클래스 타입이다.
 * 클래스 == 설계도 == 데이터 타입(큰타입)
 * 설계도정의 : 아파트 설계도 (설계도를 구성하는 요소...)
 * 클래스는 구체화를 통해서 사용(집을 짓는 행위)
 * 프로그램에서는 memory 올리는 것 >> new 연산자 >> 만든 것(객체:인스턴스)
 * 
 * 
 * 
 * 
 * 설계도라고 이야기 할려면
 * 1. 속성 (변수)
 * 2. 기능 (함수)
 * 설계도 == class
 */
